class Republica{

    Pessoa pessoas[];
    Despesa despesas[];
    int i = 0;
    
    
    //Metodos
    public void cadastrarPessoas(Pessoa P){
        
        pessoas[i] = p;
        i++;
    }

    public void cadastrarDespesa(Despesa d){
    
        despesas[i] = d;
        i++;
    }

    
}